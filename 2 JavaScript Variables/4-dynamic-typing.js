// JavaScript is a dynamically typed language.
// This means that a variable can hold any data type, and you can change
// its type by assigning a new value of a different type to it.

// In statically typed languages like Java or C#, each variable has a fixed type,
// so you can't change its type after it's set.

// Example:
let firstName = 'Steven';			// firstName is a string
console.log(typeof firstName);		// shows 'string'

firstName = 100;					// new firstName is a number 
console.log(typeof firstName);		// shows 'number'

firstName = true;					// now firstName is a boolean
console.log(typeof firstName);		// shows 'boolean'
